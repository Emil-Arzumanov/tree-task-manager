/**
 * Интерфейс задачи (Task) представляет структуру задачи в приложении
 * Поддерживает вложенность через массив дочерних задач
 */
export interface Task {
	/** Уникальный идентификатор задачи */
	id: string;

	/** Заголовок задачи */
	title: string;

	/** Подробное описание задачи */
	text: string;

	/** Флаг выполнения задачи */
	completed: boolean;

	/** Флаг свернутости (скрыты ли дочерние задачи) */
	collapsed: boolean;

	/** Массив дочерних задач (подзадач) */
	children: Task[];

	/** Опциональный идентификатор родительской задачи */
	parentId?: string | null;
}

/**
 * Перечисление режимов темы оформления приложения
 */
export enum ThemeModeEnum {
	/** Наследовать тему от системных настроек */
	inherit = "inherit",

	/** Темная тема */
	dark = "dark",

	/** Светлая тема */
	light = "light",
}
